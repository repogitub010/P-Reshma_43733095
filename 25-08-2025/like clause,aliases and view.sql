#Aliases
select worker_id as ID ,first_name as Worker_Name,Department as Worker_Department from Worker;
select worker_id as ID ,first_name as Worker_Name from Worker where department='admin';

# Like Clause
select * from Worker where last_name like 'S_n_%';
select first_name,salary from Worker where salary like '1_%';

#View
